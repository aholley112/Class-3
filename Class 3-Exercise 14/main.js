//Write a JavaScript program that determines whether a number is positive, negative, or zero. Display a message on the console indicating the type of the number.

let number = prompt("Enter a number:");
if (number > 0) {
    
    console.log("This number is positive.");
} 
else if (number < 0) {
   
    console.log("This number is negative.");
} else {
  
    console.log("This number is zero.");
}